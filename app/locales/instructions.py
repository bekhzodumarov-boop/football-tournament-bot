INSTRUCTION_PLAYER = """📖 <b>Инструкция для игрока</b>

<b>1️⃣ Регистрация</b>
Напиши /register → введи своё имя → выбери позицию (вратарь / защитник / полузащитник / нападающий).

<b>2️⃣ Запись на игру</b>
Главное меню → «⚽ Ближайшая игра» → нажми «Записаться» → прочитай Регламент → «✅ Записаться».
Если мест нет — попадёшь в лист ожидания. При отказе нажми «❌ Отказаться».

<b>3️⃣ Подтверждение за 2 часа до игры</b>
Бот пришлёт уведомление — подтверди что придёшь.
Если опаздываешь — нажми «⏰ Опоздаю». Если не придёшь — «❌ Не приду».
Это важно! Неподтверждённые участники могут быть заменены из листа ожидания.

<b>4️⃣ Оплата</b>
Уточни стоимость у организатора. Номер карты придёт в отдельном сообщении от бота — переведи сумму заранее.

<b>5️⃣ Своя команда в день игры</b>
Главное меню → «👥 Моя команда» → узнаешь цвет команды и состав партнёров.

<b>6️⃣ После игры</b>
Бот пришлёт опрос — оцени партнёров от 1 до 10. Оценки влияют на рейтинг игроков. Голосование анонимное.

<b>7️⃣ Твоя статистика</b>
• «📊 Моя статистика» — голы, карточки, игры, надёжность
• «🏆 Таблица» — турнирная таблица
• «⭐ Бомбардиры» — топ голеадоров лиги
• «👤 Профиль» — твой рейтинг и баланс

<b>💡 Полезные команды:</b>
/start — главное меню
/register — регистрация
/game — ближайшая игра
/cancel — отменить текущее действие"""


